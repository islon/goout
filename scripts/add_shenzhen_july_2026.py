def main():
    with open(SZ_FILE, 'r', encoding='utf-8') as f:
        data = json.load(f)
    before = len(data)

    # --- 清理重复: 沧溟载艺 泛化 venue 副本 ---
    removed = [x for x in data
               if '沧溟载艺' in x.get('title', '') and x.get('venue') == '南山博物馆']
    data = [x for x in data
            if not ('沧溟载艺' in x.get('title', '') and x.get('venue') == '南山博物馆')]

    # --- 去重保护: 跳过已存在的同名活动 ---
    existing_titles = {x.get('title') for x in data}
    added = 0
    skipped = 0
    for a in new_activities:
        if a['title'] in existing_titles:
            skipped += 1
            continue
        data.append(a)
        existing_titles.add(a['title'])
        added += 1

    with open(SZ_FILE, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)

    print(f"深圳活动: {before} → {len(data)}")
    print(f"  新增: {added}  (跳过已存在: {skipped})")
    print(f"  删除重复(沧溟载艺泛化副本): {len(removed)}")


if __name__ == '__main__':
    main()
