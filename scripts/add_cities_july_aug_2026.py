def main():
    total_before = 0
    total_after = 0
    total_added = 0
    total_skipped = 0
    for city_en, acts in NEW.items():
        fp = os.path.join(OUT_DIR, 'exhibitions_%s.json' % city_en)
        with open(fp, 'r', encoding='utf-8') as f:
            data = json.load(f)
        before = len(data)
        total_before += before
        existing_titles = {x.get('title') for x in data}
        added = skipped = 0
        for a in acts:
            if a['title'] in existing_titles:
                skipped += 1
                continue
            data.append(a)
            existing_titles.add(a['title'])
            added += 1
        with open(fp, 'w', encoding='utf-8') as f:
            json.dump(data, f, ensure_ascii=False, indent=2)
        total_after += len(data)
        total_added += added
        total_skipped += skipped
        print("  %-10s %4d -> %4d  (新增 %2d, 跳过 %d)" % (city_en, before, len(data), added, skipped))
    print("\n合计: 新增 %d 条 (跳过已存在 %d 条), 文件记录 %d -> %d" %
          (total_added, total_skipped, total_before, total_after))


if __name__ == '__main__':
    main()
