def _normalize_name(name):
    """去除中英文括号内容及首尾空格，便于 '深圳博物馆（历史民俗馆）' 归一为 '深圳博物馆'。"""
    if not name:
        return ''
    s = str(name)
    # 去掉英文括号及中文括号内容
    for op, cl in [('（', '）'), ('(', ')')]:
        while op in s and cl in s:
            i = s.index(op)
            j = s.index(cl, i)
            s = s[:i] + s[j + 1:]
    return s.strip()


def _load_venue_district():
    """从 venue_info.json 读 name/source → district（仅作为可选增强）。"""
    cache = {}
    path = os.path.join(PROJECT_ROOT, 'output', 'venue_info.json')
    if not os.path.exists(path):
        return cache
    try:
        with open(path, 'r', encoding='utf-8') as f:
            venues = json.load(f)
    except Exception:
        return cache
    if isinstance(venues, dict):
        venues = list(venues.values())
    for v in venues:
        if not isinstance(v, dict):
            continue
        d = v.get('district')
        if d:
            if v.get('name'):
                cache[v['name']] = d
            if v.get('source'):
                cache[v['source']] = d
    return cache


_VENUE_DISTRICT_CACHE = None


def resolve_district(city=None, source=None, venue=None, address=None, description=None):
    """返回区县名（含"区/新区"后缀）或 '其他'。"""
    global _VENUE_DISTRICT_CACHE
    if _VENUE_DISTRICT_CACHE is None:
        _VENUE_DISTRICT_CACHE = _load_venue_district()

    # 1) venue_info 已登记
    for key in (venue, source):
        if key and key in _VENUE_DISTRICT_CACHE:
            return _VENUE_DISTRICT_CACHE[key]

    # 2) 命中 SOURCE_TO_DISTRICT（先归一化名称再查）
    for key in (source, venue):
        if not key:
            continue
        if key in SOURCE_TO_DISTRICT:
            return SOURCE_TO_DISTRICT[key]
        norm = _normalize_name(key)
        if norm and norm in SOURCE_TO_DISTRICT:
            return SOURCE_TO_DISTRICT[norm]

    # 3) 关键字兜底（场馆名 / 地址 / 描述）
    text = ' '.join(str(x) for x in (venue, address, description) if x)
    for kw, dist in DISTRICT_KEYWORDS:
        if kw in text:
            return dist

    return '其他'


