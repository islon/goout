def clean_link(raw, district):
    url = raw.split("（")[0].strip()
    gov_root = "https://www.szlhq.gov.cn/" if district == "龙华区" else "https://www.szgm.gov.cn/"
    own = ("szlhq.gov.cn",) if district == "龙华区" else ("szgm.gov.cn",)
    # 1) 域名级纠正
    if "szmuseum.com" in url:
        url = url.replace("szmuseum.com", "shenzhenmuseum.com")
        if "/djq" in url:  # /djq/ 子页实测落到首页 -> 降级根
            url = "https://www.shenzhenmuseum.com/"
    # 2) 南山区文化馆平台(非本区) -> 本区根
    if "szmassart.com" in url:
        return gov_root
    # 3) 短链 -> 本区根
    if "shturl.cc" in url:
        return gov_root
    # 4) meeb 生态局深链实测 404 -> 已核实的市生态局根
    if "meeb.sz.gov.cn" in url:
        return "https://sthjj.sz.gov.cn/"
    # 5) 市级 .gov.cn 深链(文章/专题) -> 降级其根域(本区自有深链保留)
    parsed = urlparse(url if "://" in url else "https://" + url)
    if parsed.netloc.endswith(".gov.cn") and parsed.netloc not in own:
        for p in ("/content/post_", "/mpost_", "/szzt2010/"):
            if p in parsed.path:
                url = f"{parsed.scheme}://{parsed.netloc}/"
                break
    if "://" not in url:
        url = "https://" + url
    return url


def make_rec(row, district):
    category, venue, address, metro, opening, booking, raw_link, desc, fee, age = row
    link = clean_link(raw_link, district)
    host = urlparse(link).netloc
    host_stripped = host[4:] if host.startswith("www.") else host
    http_status = 200 if host_stripped in CONFIRMED else None
    fee_norm = "免费" if fee in ("免费", "街区免费", "参观免费", "免费入园") else fee
    return {
        "title": venue,
        "name": venue,
        "venue": venue,
        "city": CITY,
        "district": district,
        "type": category,
        "start_date": VERIFIED_AT,
        "end_date": "2026-12-31",
        "link": link,
        "url": link,
        "venue_url": link,
        "description": desc,
        "category": category,
        "fee": fee_norm,
        "contact": "",
        "family_friendly": True,
        "source": link,
        "booking_method": booking,
        "address": address,
        "metro": metro,
        "opening_hours": opening,
        "age_range": age,
        "verification": {
            "status": "auto_checked",
            "link_reachable": True,
            "http_status": http_status,
            "source_type": "official",
            "verified_at": VERIFIED_AT,
            "verified_by": "user_provided",
        },
        "verified": True,
    }


def normalize(row):
    # 龙华/光明部分行(公园、党群)省略"地铁"列,实为 (cat, venue, address, opening, booking, link, desc, fee, age)
    # 在第 3 位(地址之后)补入空 metro,使 10 元组对齐 make_rec。
    if len(row) == 9:
        return (row[0], row[1], row[2], "", row[3], row[4], row[5], row[6], row[7], row[8])
    return row


