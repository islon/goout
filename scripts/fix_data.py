def generate_date(year, month, day):
    return f"{year}-{month:02d}-{day:02d}"


def get_activity_type(activity_name):
    base_name = activity_name
    if '(' in activity_name and ')' in activity_name:
        base_name = activity_name[:activity_name.rfind('(')].strip()
    return ACTIVITY_TYPE_MAPPING.get(base_name, '展览')


def get_activity_description(activity_name):
    base_name = activity_name
    if '(' in activity_name and ')' in activity_name:
        base_name = activity_name[:activity_name.rfind('(')].strip()
    
    if base_name in ACTIVITY_NAME_DESCRIPTIONS:
        return ACTIVITY_NAME_DESCRIPTIONS[base_name]
    
    activity_type = get_activity_type(activity_name)
    descriptions = ACTIVITY_DESCRIPTIONS.get(activity_type, ACTIVITY_DESCRIPTIONS['展览'])
    return random.choice(descriptions)


def generate_events():
    all_events = []
    seen = set()
    
    all_venues = list(VENUES.items())
    
    total_events = sum(config['count'] for config in VENUES.values())
    
    events_per_venue = {}
    for source, config in VENUES.items():
        events_per_venue[source] = {
            'config': config,
            'index': 0,
            'activities': config['activities'][:]
        }
    
    date_pool = []
    
    for month in range(1, 13):
        if month == 2:
            days_in_month = list(range(1, 29))
        elif month in [4, 6, 9, 11]:
            days_in_month = list(range(1, 31))
        else:
            days_in_month = list(range(1, 32))
        
        for day in days_in_month:
            date_pool.append((2026, month, day))
    
    random.shuffle(date_pool)
    
    date_index = 0
    
    while len(all_events) < total_events:
        random.shuffle(all_venues)
        
        for source, config in all_venues:
            if len(all_events) >= total_events:
                break
            
            venue_data = events_per_venue[source]
            if venue_data['index'] >= config['count']:
                continue
            
            year, month, day = date_pool[date_index % len(date_pool)]
            date_index += 1
            
            activity_idx = venue_data['index'] % len(venue_data['activities'])
            activity_name = venue_data['activities'][activity_idx]
            
            start_date = generate_date(year, month, day)
            
            if month == 12 and day > 24:
                end_month = 1
                end_year = 2027
                end_day = day - 24
            else:
                end_month = month
                end_year = year
                end_day = day + random.choice([0, 1, 2])
                if end_day > 28 and month == 2:
                    end_day = 28
                elif end_day > 30 and month in [4, 6, 9, 11]:
                    end_day = 30
                elif end_day > 31:
                    end_day = 31
            
            end_date = generate_date(end_year, end_month, end_day)
            
            key = f"{activity_name}|{start_date}|{config['name']}"
            if key in seen:
                activity_name = f"{activity_name} ({venue_data['index'] + 1})"
                key = f"{activity_name}|{start_date}|{config['name']}"
            
            seen.add(key)
            
            time_str = random.choice(config['times'])
            activity_type = get_activity_type(activity_name)
            desc_base = get_activity_description(activity_name)
            description = f"{config['name']}将于{start_date}举办{activity_name}。活动时间：{time_str}。{desc_base}"
            
            is_family_friendly = True
            base_activity_name = activity_name.replace('(深圳)', '').replace('(宝安)', '').strip()
            
            NOT_FAMILY_FRIENDLY_KEYWORDS = [
                '股票', '基金', '投资', '理财', '金融', '消费金融', '证券', '期货',
                '房地产', '供应链', '物流装备', '五金机电', '建筑建材', '电池储能',
                '光伏', '智慧能源', '智能交通', '轨道交通', '氢能', '节能环保',
                '康复护理', '养老', '葡萄酒', '酒店用品', '零售商业', '电商贸易',
                '国际贸易', '工业设计', '照明灯饰', '光电技术', '显示技术',
                '触控显示', '医疗器械', '美容美发', '咖啡茶饮', '餐饮服务',
                '茶叶博览'
            ]
            
            if any(keyword in base_activity_name for keyword in NOT_FAMILY_FRIENDLY_KEYWORDS):
                is_family_friendly = False
            
            all_events.append({
                'name': activity_name,
                'venue': config['name'],
                'start_date': start_date,
                'end_date': end_date,
                'url': config['url'],
                'contact': random.choice(config['contacts']),
                'description': description,
                'source': source,
                'types': [activity_type],
                'fee': random.choice(['免费', '收费']) if activity_type == '展览' else '免费',
                'family_friendly': is_family_friendly
            })
            
            venue_data['index'] += 1
    
    all_events.sort(key=lambda e: e['start_date'])
    
    return all_events


