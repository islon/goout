def generate_date(year, month, day):
    return f"{year}-{month:02d}-{day:02d}"


def generate_events():
    all_events = []
    seen = set()
    
    all_venues = list(VENUES.items())
    
    total_events = sum(config['count'] for config in VENUES.values())
    
    events_per_venue = {}
    for source, config in VENUES.items():
        events_per_venue[source] = {
            'config': config,
            'index': 0,
            'activities': config['activities'][:]
        }
    
    date_pool = []
    for month in range(7, 13):
        days_in_month = [1, 8, 15, 22, 29]
        if month in [9, 11]:
            days_in_month = [1, 8, 15, 22]
        
        for day in days_in_month:
            date_pool.append((2026, month, day))
    
    for month in range(1, 7):
        days_in_month = [1, 8, 15, 22, 29]
        if month == 2:
            days_in_month = [1, 8, 15, 22]
        elif month in [4, 6]:
            days_in_month = [1, 8, 15, 22]
        
        for day in days_in_month:
            date_pool.append((2026, month, day))
    
    date_index = 0
    
    while len(all_events) < total_events:
        random.shuffle(all_venues)
        
        for source, config in all_venues:
            if len(all_events) >= total_events:
                break
            
            venue_data = events_per_venue[source]
            if venue_data['index'] >= config['count']:
                continue
            
            year, month, day = date_pool[date_index % len(date_pool)]
            date_index += 1
            
            activity_idx = venue_data['index'] % len(venue_data['activities'])
            activity_name = venue_data['activities'][activity_idx]
            
            start_date = generate_date(year, month, day)
            
            if month == 12 and day > 24:
                end_month = 1
                end_year = 2027
                end_day = day - 24
            else:
                end_month = month
                end_year = year
                end_day = day + random.choice([0, 1, 2])
                if end_day > 28 and month == 2:
                    end_day = 28
                elif end_day > 30 and month in [4, 6, 9, 11]:
                    end_day = 30
                elif end_day > 31:
                    end_day = 31
            
            end_date = generate_date(end_year, end_month, end_day)
            
            key = f"{activity_name}|{start_date}|{config['name']}"
            if key in seen:
                activity_name = f"{activity_name} ({venue_data['index'] + 1})"
                key = f"{activity_name}|{start_date}|{config['name']}"
            
            seen.add(key)
            
            time_str = random.choice(config['times'])
            desc_base = random.choice(config['descriptions'])
            description = f"{config['name']}将于{start_date}举办{activity_name}。活动时间：{time_str}。{desc_base}"
            
            all_events.append({
                'name': activity_name,
                'venue': config['name'],
                'start_date': start_date,
                'end_date': end_date,
                'url': config['url'],
                'contact': random.choice(config['contacts']),
                'description': description,
                'source': source
            })
            
            venue_data['index'] += 1
    
    all_events.sort(key=lambda e: e['start_date'])
    
    return all_events


def main():
    events = generate_events()
    
    print(f"生成了 {len(events)} 条活动数据")
    
    source_counts = {}
    for event in events:
        source = event['source']
        source_counts[source] = source_counts.get(source, 0) + 1
    
    print("各地点活动数:")
    for source, count in source_counts.items():
        print(f"  {VENUES[source]['name']}: {count}")
    
    upcoming_count = 0
