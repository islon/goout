def validate_venue(venue):
    """校验单个场馆数据是否合规，返回错误信息列表（空列表表示通过）。"""
    errors = []
    if not venue.get("name"):
        errors.append("缺少 name")
    if venue.get("fee") not in VALID_FEES:
        errors.append(f"fee 值非法: {venue.get('fee')}")
    desc = venue.get("description", "")
    if len(desc) < 10:
        errors.append(f"description 不足10字: {desc}")
    hl = venue.get("highlights")
    if not isinstance(hl, list) or len(hl) == 0:
        errors.append("highlights 非数组或为空")
    for field in ("source", "city", "district", "type", "address", "transport"):
        if not venue.get(field):
            errors.append(f"缺少 {field}")
    return errors


def main():
    # 1. 构建所有新场馆列表并校验数量与字段
    new_venues = []
    city_counts = {}

    for city, source, data_list in ALL_CITY_DATA:
        city_venues = []
        for item in data_list:
            venue = build_venue(item, city, source)
            errs = validate_venue(venue)
            if errs:
                print(f"  ⚠️ 校验失败 [{venue.get('name', '?')}]: {errs}")
            city_venues.append(venue)
        city_counts[city] = len(city_venues)
        new_venues.extend(city_venues)

    print("=" * 60)
    print("新增城市场馆数据校验结果：")
    for city, count in city_counts.items():
        status = "✅" if count >= MIN_PER_CITY else "❌"
        print(f"  {status} {city}: {count} 个场馆（要求 ≥{MIN_PER_CITY}）")
    print(f"  合计待添加: {len(new_venues)} 个")
    print("=" * 60)

    # 2. 读取现有 venue_info.json
    if not os.path.exists(OUTPUT_FILE):
        print(f"❌ 找不到文件: {OUTPUT_FILE}")
        sys.exit(1)

    with open(OUTPUT_FILE, 'r', encoding='utf-8') as f:
        venues = json.load(f)

    print(f"现有场馆总数: {len(venues)}")

    # 3. 按 name 去重并追加
    existing_names = {v['name'] for v in venues}
    added_count = 0
    skipped_count = 0
    skipped_by_city = Counter()

    for venue in new_venues:
        if venue['name'] in existing_names:
            skipped_count += 1
            skipped_by_city[venue['city']] += 1
            continue
        venues.append(venue)
        existing_names.add(venue['name'])
        added_count += 1

    # 4. 写回文件
    with open(OUTPUT_FILE, 'w', encoding='utf-8') as f:
        json.dump(venues, f, ensure_ascii=False, indent=2)

    print(f"\n添加完成：新增 {added_count} 个，跳过重复 {skipped_count} 个")
    if skipped_count:
        print("各城市跳过重复数量：")
        for city, cnt in sorted(skipped_by_city.items()):
            print(f"  {city}: {cnt} 个重复")

    # 5. 输出各城市场馆数量统计
    print("\n" + "=" * 60)
    print("各城市场馆数量统计（含已有数据）：")
    all_city_counts = Counter(v.get('city', '未知') for v in venues)
    for city in ['shenzhen', 'beijing', 'guangzhou', 'shanghai', 'hangzhou',
                 'chengdu', 'nanjing', 'wuhan', 'xian', 'chongqing']:
        cnt = all_city_counts.get(city, 0)
        mark = " ← 本次新增" if city in city_counts else ""
        print(f"  {city}: {cnt} 个{mark}")

    other_count = sum(c for city, c in all_city_counts.items()
                      if city not in ['shenzhen', 'beijing', 'guangzhou',
                                      'shanghai', 'hangzhou', 'chengdu',
                                      'nanjing', 'wuhan', 'xian', 'chongqing'])
    if other_count:
        print(f"  其他城市: {other_count} 个")

    print(f"\n场馆总数: {len(venues)} 个")
    print("=" * 60)


if __name__ == '__main__':
    main()