        activities.append({
            "title": title,
            "venue": venue,
            "city": "beijing",
            "start_date": start_date,
            "end_date": end_date,
            "link": link,
            "description": description,
            "fee": fee,
            "source": source,
            "family_friendly": True
        })
        existing_titles.add(title)

